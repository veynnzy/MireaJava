public class Human
{
    class Head
    {
        private float lenght, width;

        Head(float l, float w)
        {
            lenght = l;
            width = w;
        }

        public float getLenght()
        {
            return lenght;
        }

        public float getWidth()
        {
            return width;
        }

        public void setLenght(float l)
        {
            lenght = l;
        }

        public void setWidth(float w)
        {
            width = w;
        }
    }

    class Hand
    {
        private float lenght, width;

        Hand(float l, float w)
        {
            lenght = l;
            width = w;
        }
        public float getLenght()
        {
            return lenght;
        }

        public float getWidth()
        {
            return width;
        }

        public void setLenght(float l)
        {
            lenght = l;
        }

        public void setWidth(float w)
        {
            width = w;
        }
    }

    class Leg
    {
        private float lenght, width;

        Leg(float l, float w)
        {
            lenght = l;
            width = w;
        }
        public float getLenght()
        {
            return lenght;
        }

        public float getWidth()
        {
            return width;
        }

        public void setLenght(float l)
        {
            lenght = l;
        }

        public void setWidth(float w)
        {
            width = w;
        }
    }


}
